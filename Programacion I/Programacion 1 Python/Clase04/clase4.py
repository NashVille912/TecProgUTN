a = 4
b = 4

# Igual que
resultado = a == b
print(f"A es Igual que B: {resultado}")

# Diferente que
resultado = a != b
print(f"A es Diferente que B: {resultado}")

# mayor que
resultado = a > b
print(f"A es mayor que B: {resultado}")
# mayor o igual que
resultado = a >= b
print(f"A es mayor o igual que B: {resultado}")

# menor que
resultado = a < b
print(f"A es menor que B: {resultado}")
# menor o igual que
resultado = a <= b
print(f"A es menor o igual que B: {resultado}")
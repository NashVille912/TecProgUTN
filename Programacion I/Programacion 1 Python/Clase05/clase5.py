# Operadores Logicos

# AND (Las dos condiciones True para ser verdadero)
a = False
b = True

resultado = a and b
print(resultado)

# OR (Las una de las dos condiciones True para ser verdadero)

resultado = a or b
print(resultado)

# NOT (Operador UNARIO, Niega la sentencia)
resultado = (not a) and b
print(resultado)